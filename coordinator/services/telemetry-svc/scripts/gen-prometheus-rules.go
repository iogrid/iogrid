//go:build genrules

// gen-prometheus-rules renders the SLO catalogue under ../slo into a
// kustomize-loadable multi-doc YAML stream of PrometheusRule CRDs.
//
// Run via:
//
//	go run -tags=genrules ./scripts/gen-prometheus-rules.go > ../../../infra/k8s/base/telemetry-svc/prometheusrules.yaml
//
// The build tag keeps it out of `go build ./...` so CI doesn't try to
// compile it as part of the service binary.
package main

import (
	"fmt"
	"os"

	"github.com/iogrid/iogrid/coordinator/services/telemetry-svc/internal/slo"
)

func main() {
	cat, err := slo.Load("./slo")
	if err != nil {
		fmt.Fprintf(os.Stderr, "load catalogue: %v\n", err)
		os.Exit(1)
	}
	rules := cat.PrometheusRules("iogrid")
	rules = append(rules, slo.StandaloneRules("iogrid")...)
	out, err := slo.MarshalPrometheusRules(rules)
	if err != nil {
		fmt.Fprintf(os.Stderr, "marshal: %v\n", err)
		os.Exit(1)
	}
	fmt.Println("# AUTO-GENERATED by coordinator/services/telemetry-svc/scripts/gen-prometheus-rules.go")
	fmt.Println("# Regenerate via: cd coordinator/services/telemetry-svc && go run -tags=genrules ./scripts/gen-prometheus-rules.go > ../../../infra/k8s/base/telemetry-svc/prometheusrules.yaml")
	os.Stdout.Write(out)
}
