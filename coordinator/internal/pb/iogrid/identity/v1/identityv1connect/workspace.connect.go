// Workspace service: the bounded-context that links a User to billing
// + workloads + API keys. Every paid resource in iogrid is owned by a
// Workspace, not by a User directly; this gives us multi-tenant
// permission boundaries (one user can belong to many workspaces) and
// matches Stripe's "customer" granularity.
//
// On first authentication identity-svc auto-mints a "personal
// workspace" for the user with the user as OWNER. The user can rename
// it, invite collaborators, upgrade the plan, or create additional
// workspaces from the management plane.
//
// Authorization rules:
//   * GetWorkspace / ListMembers       — any member (OWNER, ADMIN, BILLING_ONLY, READ_ONLY)
//   * UpdateWorkspace                  — OWNER or ADMIN
//   * AddMember / RemoveMember /
//     UpdateMemberRole                 — OWNER or ADMIN
//   * DeleteWorkspace                  — OWNER only
//   * CreateWorkspace / ListWorkspaces — any authenticated user

